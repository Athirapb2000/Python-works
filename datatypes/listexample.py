# list slicing
list1 = [1, 2, 3, 4, 5]
print(list1)
print(list1[3])
print(list1[2:5])
print('\n')

# nested list
list2 = ['welcome', [8, 4, 6], 'Hi', 5.2]
print(list2)
print(list2[0][0])
print(list2[1][0])
print(list2[2][1])
print(list2[2])
print(list2[2][0:2])
print(list2[3])

print(list1 + list2)
